package oops.inheritance;

public class DemoA {

    public void m1(){

        System.out.println("This is M1 method of class DemoA");
    }

    private void m2(){

        System.out.println("This is Private method of DemoA");
    }

    protected void m3(){

        System.out.println("This is protected method M3 of DemoA");
    }


}
